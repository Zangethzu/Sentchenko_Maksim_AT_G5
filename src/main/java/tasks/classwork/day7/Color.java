package main.java.tasks.classwork.day7;

public class Color {

    private final String color;
    private final int hex;

    public Color(String color, int hex) {
        this.color = color;
        this.hex = hex;
    }

    public String getColor() {
        return color;
    }

    public int getHex() {
        return hex;
    }
}
